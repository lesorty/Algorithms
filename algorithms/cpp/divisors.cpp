void findivisors(ll n, vector<ll> &divisores){
    for(ll i = 1; (i*i) <= n; i++){
        if(n % i == 0){
            if(n/i == i) divisores.pb(i);

            else{
                divisores.pb(i);
                divisores.pb((n/i));
            }
        }
    }
}



long long numberOfDivisors(long long num) {
    long long total = 1;
    for (int i = 2; (long long)i * i <= num; i++) {
        if (num % i == 0) {
            int e = 0;
            do {
                e++;
                num /= i;
            } while (num % i == 0);
            total *= e + 1;
        }
    }
    if (num > 1) {
        total *= 2;
    }
    return total;
}